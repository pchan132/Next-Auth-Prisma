import { Geist, Geist_Mono } from "next/font/google";
import "./globals.css";

import SessionProvider from "./components/SessionProvider"; // นำเข้า SessionProvider สำหรับจัดการ session ของผู้ใช้
import { getServerSession } from "next-auth";

const geistSans = Geist({
  variable: "--font-geist-sans",
  subsets: ["latin"],
});

const geistMono = Geist_Mono({
  variable: "--font-geist-mono",
  subsets: ["latin"],
});

export const metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default async function RootLayout({ children }) {
  const session = await getServerSession(); // รับ session ของผู้ใช้จากเซิร์ฟเวอร์

  return (
    <html lang="en">
      <body
        className={`${geistSans.variable} ${geistMono.variable} antialiased`}
      > 
      {/* ใช้ SessionProvider เพื่อให้สามารถเข้าถึง session ของผู้ใช้ได้ในคอมโพเนนต์ลูก */}
        <SessionProvider session={session}>
          {children}
        </SessionProvider>
      </body>
    </html>
  );
}
